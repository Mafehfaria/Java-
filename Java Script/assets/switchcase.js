let março="outono";
switch (março) {
    case "março":
        console.log("Começa o outono");
        break;
        case "junho":
            console.log("Começa o inverno");
            break;
            case "setembro":
                console.log("Começa a primavera");
                break;
                case "dezembro":
                    console.log("Começa o verão");
                    break;

                default:
                    console.log("O mês não esta na lista");
                    break;
}